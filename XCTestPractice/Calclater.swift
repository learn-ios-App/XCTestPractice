import Foundation

class Calclater {
    func add(a: Int, b: Int) -> Int {
        return a + b
    }
    func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    func mul(a: Int, b: Int) -> Int {
        return a * b
    }
}
